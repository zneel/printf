#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*head;
	t_list	*tail;
	t_list	*new;

	if (!lst)
		return (NULL);
	head = NULL;
	tail = NULL;
	while (lst)
	{
		new = ft_lstnew(f(lst->content));
		if (!new)
		{
			ft_lstclear(&head, del);
			return (NULL);
		}
		if (tail)
			tail->next = new;
		else
			head = new;
		tail = new;
		lst = lst->next;
	}
	return (head);
}
